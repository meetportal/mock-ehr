export const dateIsPast = (date: Date) => {
  return date.getTime() > new Date().getTime()
}
export const addDaysToDate = (date: Date, days: number) => {
  const newDate = new Date(date)
  newDate.setHours(17,59,59,999)
  return new Date(date.getTime() + days * 24 * 60 * 60 * 1000)
}
export const isDateWithinMinutes = (date: Date, min: number) => {
  const now = new Date()
  const diff = Math.abs(now.getTime() - date.getTime())
  return diff < min * 60 * 1000
}
export const howManyMinutesAwayFromDate = (date: Date) => {
  const now = new Date()
  const diff = now.getTime() - date.getTime()
  return Math.floor(diff / (60 * 1000))
}
export const createDemoPatientVisits = (qty = 10, patients: any[]) => {
  const visits: any[] = []
  var start = new Date()
  start.setHours(8,59,59,999)
  var end = new Date()
  end.setHours(17,59,59,999)
  for (let i = 0; i < qty; i++) {
    const date =  randomDateWithinRange(start, end)
    const next = dateIsPast(date) ? addDaysToDate(date, 60) : ''
    const visit = {
      patientId: patients[Math.floor(Math.random() * patients.length)].id,
      date,
      purpose: purposes[Math.floor(Math.random() * purposes.length)],
      diagnosis: diagnosises[Math.floor(Math.random() * diagnosises.length)],
      prescriptions: prescriptions[Math.floor(Math.random() * prescriptions.length)],
      provider: providers[Math.floor(Math.random() * providers.length)],
      next,
    }
    visits.push(visit)
  }
  return visits.sort((a, b) => (a.date > b.date ? 1 : -1))
}
